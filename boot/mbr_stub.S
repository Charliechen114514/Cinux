/**
 * @file boot/mbr_stub.S
 * @brief Cinux 最小 MBR（主引导记录）存根
 *
 * 这是 Cinux 项目最初期的 MBR 实现，用于验证构建链和 QEMU 启动。
 * 当前版本只做三件事：关中断、停机、等待。
 *
 * 后续步骤会在此文件扩展为完整的 MBR bootloader。
 *
 * AT&T 语法说明：
 *   - 操作数顺序：源操作数, 目标操作数（与 Intel 语法相反）
 *   - 寄存器前缀：%
 *   - 立即数前缀：$
 *   - 内存寻址：offset(base, index, scale)
 *   - 注释格式：# 注释内容
 */

// ============================================================
// 代码段：16 位实模式代码
// ============================================================

.section .text
.code16                      // 告诉汇编器生成 16 位代码
.global _start                // 使 _start 对链接器可见

# ============================================================
# 函数名：_start
# 职责：MBR 入口点，BIOS 加载后从此处开始执行
# 输入：CS:IP = 0x0000:0x7C00（BIOS 将 MBR 加载到 0x7C00）
# 输出：无
# 影响：CLI 禁用中断，HLT 停止 CPU
# ============================================================
_start:

    # TODO: 步骤 1 说明：清屏
    # VGA 文本模式：80 列 x 25 行 = 2000 字符单元
    # 每个单元 2 字节，共 4000 字节
    mov $0xB800, %ax         # [→ax] VGA 段地址
    mov %ax, %es             # [→es] 设置 ES 段寄存器
    xor %di, %di             # [→di] 清零，ES:DI 指向显存起始位置
    mov $0x0720, %ax         # [→ax] 空格字符 (0x20) + 灰色属性 (0x07)
    mov $2000, %cx           # [→cx] 循环计数：2000 个字符单元
    cld                      # [→direction] 清除方向标志，正向递增 DI
    rep stosw                # [→es:di] 重复存储 AX 到 ES:DI，每次 DI += 2

    # TODO: 步骤 2 说明：打印字符到 VGA
    # 属性字节格式：[高4位背景][低4位前景]
    # 0x1F = 0001_1111 = 蓝底白字
    mov $0x1F, %ah           # [→ah] 属性：蓝底(0x1)白字(0xF)
    mov $'C', %al            # [→al] 字符 'C'
    mov %ax, %es:(0)         # [→es:0] 写入显存（字符 + 属性）

    # TODO: 步骤 2 说明：关中断
    # 禁用中断，防止任何中断在初始化完成前触发
    cli                      # [→flags] 清除 IF 位，禁用可屏蔽中断

    # TODO: 步骤 3 说明：停机等待
    # 停止 CPU 执行，直到收到中断（已禁用，所以实际上会永久停机）
    hlt                      # [→cpu] 停止指令执行

    # TODO: 步骤 3 说明：防止继续执行
    # 如果某种情况下 HLT 被唤醒，跳回自己继续停机
    jmp _start               # [→IP] 无限循环，确保不会执行后面的数据

// ============================================================
// 数据段和填充
// ============================================================

// .fill 填充指令会生成数据来填充空间
// 这确保 MBR 正好是 512 字节，且最后两个字节是签名

.fill 510 - (. - _start), 1, 0  # [→memory] 用 0 填充到偏移 510
                                  // 计算当前位置到 _start 的距离
                                  // 填充 510 - 已用字节数

// TODO: MBR 签名（魔数）
// BIOS 会检查最后两个字节是否为 0xAA55，否则视为无效 MBR
.word 0xAA55                     # [→memory:510] 写入小端序签名 0xAA55
                                  // 注意：.word 写入的是 0x55AA（小端序）
                                  // 磁盘上实际为 55 AA，BIOS 读取为 AA55
